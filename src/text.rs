pub fn draw_text(text: &String, x: &u16, y: &u16, color: u8, pix_buffer: &mut [[u8; 256]; 144]) {
    let mut xoffset: u16 = 0;
    let mut yoffset: u16 = 0;
    for char in text.to_uppercase().chars() {
        let sprite = match char {
            'A' => "0102101211121012101",
            'B' => "1102101211021012110",
            'C' => "1112100210021002111",
            'D' => "1102101210121012110",
            'E' => "1112100211021002111",
            'F' => "1112100211021002100",
            'G' => "1112100210121012111",
            'H' => "1012101211121012101",
            'I' => "1112010201020102111",
            'J' => "1112001200121012111",
            'K' => "1012101211021012101",
            'L' => "1002100210021002111",
            'M' => "1112111211121012101",
            'N' => "1112101210121012101",
            'O' => "1112101210121012111",
            'P' => "1112101211121002100",
            'Q' => "1112101210121112001",
            'R' => "1112101211121102101",
            'S' => "0112100201020012110",
            'T' => "1112010201020102010",
            'U' => "1012101210121012111",
            'V' => "1012101210121012010",
            'W' => "1012101210121112101",
            'X' => "1012101201021012101",
            'Y' => "1012101201020102010",
            'Z' => "1112001201021002111",
            '0' => "0112101210121012110",
            '1' => "0102110201020102111",
            '2' => "1112001211121002111",
            '3' => "1112001201120012111",
            '4' => "1012101211120012001",
            '5' => "1112100211120012110",
            '6' => "1112100211121012111",
            '7' => "1112001201020102010",
            '8' => "1112101211121012111",
            '9' => "1112101211120012001",
            ' ' => "0002000200020002000",
            '!' => "0102010201020002010",
            '"' => "0002101210120002000",
            '#' => "1012111210121112101",
            '$' => "0102110210120112010",
            '%' => "1002001201021002001",
            '&' => "0102101201021012011",
            '\''=> "0002010201020002000",
            '(' => "0012010201020102001",
            ')' => "1002010201020102100",
            '*' => "1012010210120002000",
            '+' => "0002010211120102000",
            ',' => "0002000200021002100",
            '-' => "0002000211120002000",
            '.' => "0002000200020002100",
            '/' => "0002001201021002000",
            ':' => "0002010200020102000",
            ';' => "0002010200020102010",
            '<' => "0012010210020102001",
            '=' => "0002111200021112000",
            '>' => "1002010200120102100",
            '?' => "1112001201120002010",
            '@' => "1112101210121002111",
            '[' => "0112010201020102011",
            '\\'=> "0002100201020012000",
            ']' => "1102010201020102110",
            '^' => "0102101200020002000",
            '_' => "0002000200020002111",
            '`' => "1002010200020002000",
            '{' => "0012010211020102001",
            '|' => "0102010201020102010",
            '}' => "1002010201120102100",
            '~' => "0002011211020002000",
            '\n'=> "0002000200020002003",
             _  => "0002000200020002000"
        };
        let mut linebreak = false;
        for trit in sprite.chars() {
            match trit {
                '1' => {
                    if (x+xoffset) < 256 && (y+yoffset) < 144 {
                        pix_buffer[(y+yoffset) as usize][(x+xoffset) as usize] = color;
                    }
                    xoffset += 1;
                }
                '2' => {
                    yoffset += 1;
                    xoffset -= 3;
                }
                '3' => {
                    linebreak = true;
                }
                _ => {
                    xoffset += 1;
                }
            }
        }
        if linebreak {
            yoffset += 2;
            xoffset = 0;
        } else {
            yoffset -= 4;
            xoffset += 1;
        };
    }
}